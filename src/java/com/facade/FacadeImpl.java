package com.facade;

/**
 *
 * @author Kolade
 */
public class FacadeImpl {

    private final Shape circle;
    private final Shape rectangle;
    private final Shape square;

    public FacadeImpl() {
        circle = new Circle();
        rectangle = new Rectangle();
        square = new Square();
    }

    public void drawCircle() {
        circle.draw();
    }

    public void drawRectangle() {
        rectangle.draw();
    }

    public void drawSquare() {
        square.draw();
    }
}
